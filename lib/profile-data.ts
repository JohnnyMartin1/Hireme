export const GRADUATION_YEARS = (() => {
  const currentYear = new Date().getFullYear();
  const years = [];
  
  // Generate years from 1980 to 6 years in the future
  for (let year = currentYear + 6; year >= 1980; year--) {
    years.push(year.toString());
  }
  
  return years;
})();

